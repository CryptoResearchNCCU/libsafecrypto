Suite *aes_suite(void)
{
    Suite *s;
    TCase *tc_core_128, *tc_core_192, *tc_core_256;

    s = suite_create("AES");

    /* Test cases */
    tc_core_128 = tcase_create("CORE_128");
    tcase_add_test(tc_core_128, test_aes_128_0);
    tcase_add_test(tc_core_128, test_aes_ctr_128);
    suite_add_tcase(s, tc_core_128);

    tc_core_192 = tcase_create("CORE_192");
    tcase_add_test(tc_core_192, test_aes_192_0);
    tcase_add_test(tc_core_128, test_aes_ctr_192);
    suite_add_tcase(s, tc_core_192);

    tc_core_256 = tcase_create("CORE_256");
    tcase_add_test(tc_core_256, test_aes_256_0);
    tcase_add_test(tc_core_128, test_aes_ctr_256);
    suite_add_tcase(s, tc_core_256);

    return s;
}

int main(void)
{
    int number_failed;
    Suite *s;
    SRunner *sr;

    s = aes_suite();
    sr = srunner_create(s);

    srunner_run_all(sr, CK_NORMAL);
    number_failed = srunner_ntests_failed(sr);
    srunner_free(sr);
    return (number_failed == 0) ? EXIT_SUCCESS : EXIT_FAILURE;
}


