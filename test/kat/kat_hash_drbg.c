int main(void)
{
    int retval = EXIT_SUCCESS;
#ifndef _ENABLE_ENTROPY_ARRAY
    fprintf(stderr, "Cannot control entropy source for compliance test.\n");
    fprintf(stderr, "Recompile with _ENABLE_ENTROPY_ARRAY.\n");
#else
    SC_TIMER_INSTANCE(rnd_timer);
    SC_TIMER_CREATE(rnd_timer);

    UINT64 k;
    UINT8 *data = malloc(128);

    SC_TIMER_RESET(rnd_timer);

    prng_ctx_t *prng_ctx = NULL;
    char msg[32];

    size_t security_strength = 128;
    size_t nonce_bytes = (security_strength >> 1) >> 3;

    prng_set_entropy_array(sha256_entropy, 3*14*4);

    prng_ctx = prng_create_with_nonce(SC_ENTROPY_ARRAY,
        SC_PRNG_HASH_DRBG_SHA2_256, SC_PRNG_THREADING_NONE,
        0x00100000, sha256_nonce, nonce_bytes);
    if (NULL == prng_ctx) {
        return EXIT_FAILURE;
    }

    strcpy(msg, "HASH-DRBG-SHA-256");

    if (NULL == prng_ctx) {
        SC_TIMER_DESTROY(rnd_timer);
        free(data);
        return EXIT_FAILURE;
    }
    
    SC_TIMER_START(rnd_timer);
    prng_mem(prng_ctx, data, 128);
    SC_TIMER_STOP(rnd_timer);

    printf("Data returned:\n");
    for (size_t i=0; i<128; i++) {
        printf("%02X ", data[i]);
        if (7 == (i&0x7)) printf("\n");
    }
    printf("\n");
    
    for (size_t i=0; i<128; i++) {
        if (sha256_rnd_val[i] != data[i]) {
            printf("ERROR! SHA-256 Hash-DRBG mismatch\n");
            retval = EXIT_FAILURE;
            goto out;
        }
    }

    double elapsed = SC_TIMER_GET_ELAPSED(rnd_timer);
    printf("CSPRNG time: %f (%3.3f Mbyte per sec)\n", elapsed, (double)NUM_ITER * (double)NUM_SAMPLES / (1048576UL * elapsed));
    
out:
    prng_destroy(prng_ctx);

    SC_TIMER_DESTROY(rnd_timer);
    free(data);

#endif
    return retval;
}

